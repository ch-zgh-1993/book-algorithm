// 基础编程模型： 
// 		静态方法库
// 		
// 模块化编程：
// 		java的模块化编程，是通过静态方法库实现的。许多静态方法库，库中的静态方法可以条用其他库的静态方法。
// 		优势：
// 			分解程序到模块，程序很大，模块大小仍然适中。
// 			可以共享和重用代码而无需重新实现。
// 			很容易改进代码实现，并替换旧的实现
// 			可以为解决问题建立合适的抽象模型
// 			缩小调试范围
// 
// 单元测试：
// 		main() -> 测试用例： 对代码进行全面的测试。 -> 更复杂为测试模块.
// 		
// 外部库：
// 		分为两个类型：
// 			1. 系统标准库： java.lang.*
// 			2. 导入的系统库: java.util.Arrays
// 		我们使用模块化方式编写方法库，能够扩展我们的编程模型。
// 		